# A python code to demonstrate the use of dictionary

# create a dictionary storing the price of fruits
friut_price = {'apple': 2.50, 'banana': 3.00, 'orange': 3.50}
# dictionary is a collection of key-value pairs

print("Apple", friut_price['apple'])
print("Banana", friut_price['banana'])
print("Orange", friut_price['orange'])
